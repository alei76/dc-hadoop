	@Override
	protected void map(LongWritable key, Text value, Context context) throws IOException, InterruptedException {
		String line = value.toString().replace("\r", "").replace("\n", "");
		String[] array = line.split(MRConstants.SEPERATOR_IN);
		EventLog eventLog = null;
		try{
			//日志里某些字段有换行字符，导致这条日志换行而不完整，
			//用 try-catch 过滤掉这类型的错误日志
			eventLog = new EventLog(array);
		}catch(Exception e){
			//TODO do something to mark the error here
			return;
		}
		
		String accountId = eventLog.getAccountID().trim();
		if(!eventLog.getAppID().contains("EA7D0701220EF0E4ACA26BC41B7C2AF9")
				|| !eventLog.getEventId().contains("DESelf_LevelsBegin")
				|| !set.contains(accountId)){
			return;
		}
		
		Map<String, String> map = eventLog.getArrtMap();
		String levelId = map.get("levelId");
		String startTime = map.get("startTime");
		String loginTime = map.get("loginTime");
		if(StringUtil.isEmpty(levelId)){
			return;
		}
		if(StringUtil.isEmpty(startTime)){
			if(StringUtil.isEmpty(loginTime)){
				return;
			}
			startTime = loginTime;
		}
		
		mapKeyObj.set(accountId);
		mapValObj.setOutFields(new String[]{
				levelId,
				startTime
		});
		context.write(mapKeyObj, mapValObj);
	}
	
