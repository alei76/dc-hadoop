	@Override
	protected void reduce(Text key, Iterable<Text> values, Context context) throws IOException, InterruptedException {
		for(Text value : values){
			String[] onlineArr = value.toString().split(MRConstants.SEPERATOR_IN);
			OnlineLog onlineLog = null;
			try{
				onlineLog = new OnlineLog(onlineArr);
				valFields.setOutFields(new String[]{
						onlineLog.getAccountID(),
						onlineLog.getChannel(),
						onlineLog.getCountry(),
						onlineLog.getProvince(),
						onlineLog.getBrand(),
						onlineLog.getOperSystem(),
						null == onlineLog.getMac() ? "-" : onlineLog.getMac(),
						onlineLog.getExtend_4()
				});
				valFields.setSuffix("ONLINE");
				context.write(valFields, NullWritable.get());
				
				String isNewAdd = map.get(onlineLog.getAccountID());
				if(null != isNewAdd && isNewAdd.contains("N")){
					valFields.setSuffix("NEWADD");
					context.write(valFields, NullWritable.get());
				}
				break;
			}catch(Exception e){
				//TODO do something to mark the error here
				continue;
			}
		}
	}
	
